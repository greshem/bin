sub gen_stunnel_pem()
{
print <<EOF
tar -xzvf stunnel-5.31.tar.gz 
cd stunnel-5.31/
./configure 
make cert
EOF
;
}


sub gen_https_conf()
{
    open(FILE, "https.conf") or die("open httpd.conf error \n");    
print FILE <<EOF
; TLS front-end to a web server
; listen to 444 
[https]
accept  = 444
connect = 8888
cert =/tmp/stunnel.pem
EOF
;

}
